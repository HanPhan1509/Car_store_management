    //Được triệu hồi khi một key đã được nhả ra
    private void txtSoluongKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSoluongKeyReleased
        try {
            if (txtgia.getText() != null) {
                String thanhtien = String.valueOf(
                        Long.parseLong(txtgia.getText()) * Long.parseLong(txtSoluong.getText()));
//                        Integer.parseInt(txtgia.getText()) * Integer.parseInt(txtSoluong.getText()));
                txtThanhTien.setText(thanhtien);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_txtSoluongKeyReleased

    //Hiển thị danh sách lên table
    public void hienThi() throws ClassNotFoundException {
        DefaultTableModel model = (DefaultTableModel) tbHienThiSP.getModel();
        sp.hienThiXe();
        model.setRowCount(0);
        for (Xe x : sp.dsXe) {
            String[] array = new String[]{x.getTenXe(), x.getMaLoai(), x.getXuatXu(), x.getDungTich(), x.getDonGia()};
            model.addRow(array);
        }
    }

