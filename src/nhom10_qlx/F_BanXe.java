    //Sự kiện click chuột vào table để hiện thị thông tin ra text
    private void tbHienThiSPMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHienThiSPMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbHienThiSP.getSelectedRow();
            for (int i = 0; i < tbHienThiSP.getSelectedRowCount(); i++) {
                txtTenxe.setText(tbHienThiSP.getValueAt(row, 0).toString());
                txtTenloai.setText(tbHienThiSP.getValueAt(row, 1).toString());
                txtXuatxu.setText(tbHienThiSP.getValueAt(row, 2).toString());
                txtDungtich.setText(tbHienThiSP.getValueAt(row, 3).toString());
                txtgia.setText(tbHienThiSP.getValueAt(row, 4).toString());
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tbHienThiSPMouseClicked

    //Sự kiện button Mua
    private void btnMuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMuaActionPerformed
        // TODO add your handling code here:
        try {
            //Err: Để trống text
            if (txtHoTen.getText().equals("")
                    || txtDiaChi.equals("")
                    || txtSDT.getText().equals("")
                    || txtSoluong.getText().equals("")) {
                JOptionPane.showMessageDialog(this, "Bạn vẫn chưa nhập đầy đủ thông tin");
            } //Err: Chưa chọn loại xe ở bảng
            else if (tbHienThiSP.getSelectedRowCount() == 0) {
                JOptionPane.showMessageDialog(this, "Bạn vẫn chưa chọn xe muốn mua");
            } else {
                //JOptionPane.showMessageDialog(null, "Total money:" + (Integer.parseInt(txtgia.getText()) * Integer.parseInt(txtSoluong.getText())));
//                String thanhtien = Integer.parseInt(txtgia.getText()) * Integer.parseInt(txtSoluong.getText()) + " ";
//                txtThanhTien.setText(thanhtien);
                HoaDon hd = new HoaDon();
                hd.setHoTen(txtHoTen.getText());
                hd.setDiaChi(txtDiaChi.getText());
                hd.setSdt(txtSDT.getText());
                hd.setTenXe(txtTenxe.getText());
                hd.setTenLoai(txtTenloai.getText());
                hd.setDungTich(txtDungtich.getText());
                hd.setXuatXu(txtXuatxu.getText());
                hd.setGia(txtgia.getText());
                hd.setSoLuong(txtSoluong.getText());
                hd.setThanhTien(txtThanhTien.getText());

                lsHD.themHD(hd);
                txtHoTen.setText("");
                txtDiaChi.setText("");
                txtSDT.setText("");
                txtTenxe.setText("");
                txtTenloai.setText("");
                txtXuatxu.setText("");
                txtDungtich.setText("");
                txtgia.setText("");
                txtSoluong.setText("");
                txtThanhTien.setText("");
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnMuaActionPerformed

