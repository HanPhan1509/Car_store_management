    private void tbHienThiLoaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbHienThiLoaiMouseClicked
        // TODO add your handling code here:
        try {
            int row = tbHienThiLoai.getSelectedRow();
            for (int i = 0; i < tbHienThiLoai.getSelectedRowCount(); i++) {
                txtMaloai.setText(tbHienThiLoai.getValueAt(row, 0).toString());
                txtTenLoai.setText(tbHienThiLoai.getValueAt(row, 1).toString());
                txtXuatXu.setText(tbHienThiLoai.getValueAt(row, 2).toString());

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tbHienThiLoaiMouseClicked

