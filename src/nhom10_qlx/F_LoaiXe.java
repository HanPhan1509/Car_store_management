    public void hienThi() throws ClassNotFoundException {
        DefaultTableModel model = (DefaultTableModel) tbHienThiLoai.getModel();
        lsLoaiXe.hienthiLoai();
        model.setRowCount(0);
        for (LoaiXe loaixe : lsLoaiXe.dsLoai) {
            String[] array = new String[]{loaixe.getMaLoai(), loaixe.getTenLoai(), loaixe.getXuatXu()};
            model.addRow(array);
        }
    }

